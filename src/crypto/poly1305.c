static inline void carry_propagate(uint32_t a[5]) {
    uint64_t c;
    c = a[1] >> 26;
    a[1] &= 0x3ffffff;
    a[2] += (uint32_t)c;
    c = a[2] >> 26;
    a[2] &= 0x3ffffff;
    a[3] += (uint32_t)c;
    c = a[3] >> 26;
    a[3] &= 0x3ffffff;
    a[4] += (uint32_t)c;
    c = a[4] >> 26;
    a[4] &= 0x3ffffff;
    a[0] += (uint32_t)c * 5;
    c = a[0] >> 26;
    a[0] &= 0x3ffffff;
    a[1] += (uint32_t)c;
}

void poly1305_mac(uint8_t tag[POLY1305_TAG_SIZE],
                  const uint8_t key[POLY1305_KEY_SIZE], size_t len,
                  const uint8_t msg[static len]) {
    assert(tag != NULL);
    assert(key != NULL);
    assert(msg != NULL);

    // Load bottom 16 bytes and clamp
    uint32_t t0 = load_le32(key + 0) & 0x0fffffff;
    uint32_t t1 = load_le32(key + 4) & 0x0ffffffc;
    uint32_t t2 = load_le32(key + 8) & 0x0ffffffc;
    uint32_t t3 = load_le32(key + 12) & 0x0ffffffc;

    // Load t into r as 24 bit limbs
    uint32_t r[5];
    r[0] = t0 & 0x3ffffff;
    r[1] = (t0 >> 26 | t1 << 6) & 0x3ffffff;
    r[2] = (t1 >> 20 | t2 << 12) & 0x3ffffff;
    r[3] = (t2 >> 14 | t3 << 18) & 0x3ffffff;
    r[4] = (t3 >> 8) & 0x3ffffff;

    // Constant precalculated 5 * r multiples for quick multiplication mod
    // 2^130-5
    const uint64_t r1_5 = r[1] * 5, r2_5 = r[2] * 5, r3_5 = r[3] * 5,
                   r4_5 = r[4] * 5;

    uint32_t s[4];
    for (size_t i = 0; i < 4; i++)
        s[i] = load_le32(key + 16 + i * 4);

    uint32_t a[5]    = {0}; // 5 26-bit limbs accumulator

    const uint8_t *p = msg;
    size_t remaining = len;

    while (remaining > 0) {
        size_t block_len = remaining < 16 ? remaining : 16;

        // msg[((i-1)*16)..(i*16)] | [0x01]
        uint8_t buf[17] = {0};
        memcpy(buf, p, block_len);
        buf[block_len] = 0x01;

        // n = le_bytes_to_num(...)
        uint32_t n0 = load_le32(buf + 0);
        uint32_t n1 = load_le32(buf + 4);
        uint32_t n2 = load_le32(buf + 8);
        uint32_t n3 = load_le32(buf + 12);
        uint32_t n4 = buf[16];

        // a += n
        a[0] += n0 & 0x3ffffff;
        a[1] += (n0 >> 26 | n1 << 6) & 0x3ffffff;
        a[2] += (n1 >> 20 | n2 << 12) & 0x3ffffff;
        a[3] += (n2 >> 14 | n3 << 18) & 0x3ffffff;
        a[4] += (n3 >> 8 | n4 << 24) & 0x3ffffff;

        // a = (r * a) % p

        uint64_t d[5];
        d[0] = (uint64_t)a[0] * r[0] + (uint64_t)a[1] * r4_5 +
               (uint64_t)a[2] * r3_5 + (uint64_t)a[3] * r2_5 +
               (uint64_t)a[4] * r1_5;
        d[1] = (uint64_t)a[0] * r[1] + (uint64_t)a[1] * r[0] +
               (uint64_t)a[2] * r4_5 + (uint64_t)a[3] * r3_5 +
               (uint64_t)a[4] * r2_5;
        d[2] = (uint64_t)a[0] * r[2] + (uint64_t)a[1] * r[1] +
               (uint64_t)a[2] * r[0] + (uint64_t)a[3] * r4_5 +
               (uint64_t)a[4] * r3_5;
        d[3] = (uint64_t)a[0] * r[3] + (uint64_t)a[1] * r[2] +
               (uint64_t)a[2] * r[1] + (uint64_t)a[3] * r[0] +
               (uint64_t)a[4] * r4_5;
        d[4] = (uint64_t)a[0] * r[4] + (uint64_t)a[1] * r[3] +
               (uint64_t)a[2] * r[2] + (uint64_t)a[3] * r[1] +
               (uint64_t)a[4] * r[0];

        uint64_t c;
        c    = d[0] >> 26;
        a[0] = (uint32_t)(d[0] & 0x3ffffff);
        d[1] += c;
        c    = d[1] >> 26;
        a[1] = (uint32_t)(d[1] & 0x3ffffff);
        d[2] += c;
        c    = d[2] >> 26;
        a[2] = (uint32_t)(d[2] & 0x3ffffff);
        d[3] += c;
        c    = d[3] >> 26;
        a[3] = (uint32_t)(d[3] & 0x3ffffff);
        d[4] += c;
        c    = d[4] >> 26;
        a[4] = (uint32_t)(d[4] & 0x3ffffff);
        a[0] += (uint32_t)c * 5;
        carry_propagate(a);

        p += block_len;
        remaining -= block_len;
    }

    carry_propagate(a);

    uint32_t h[5];
    uint64_t c = 5;
    for (int i = 0; i < 5; i++) {
        uint64_t t = (uint64_t)a[i] + c;
        h[i]       = (uint32_t)(t & 0x3ffffff);
        c          = t >> 26;
    }
    // c != 0 means a + 5 >= 2^130, so a >= p: use h (the reduced value).
    // c == 0 means a < p: keep a.
    uint32_t mask = (uint32_t)(-(int32_t)c);
    for (int i = 0; i < 5; i++)
        a[i] = (h[i] & mask) | (a[i] & ~mask);

    // reduce a to 4 x 32-bit words
    uint32_t g[4];
    g[0] = a[0] | a[1] << 26;
    g[1] = a[1] >> 6 | a[2] << 20;
    g[2] = a[2] >> 12 | a[3] << 14;
    g[3] = a[3] >> 18 | a[4] << 8;

    // add s with carry
    uint64_t f;
    f = (uint64_t)g[0] + s[0];
    store_le32(tag + 0, f);
    f = (uint64_t)g[1] + s[1] + (f >> 32);
    store_le32(tag + 4, f);
    f = (uint64_t)g[2] + s[2] + (f >> 32);
    store_le32(tag + 8, f);
    f = (uint64_t)g[3] + s[3] + (f >> 32);
    store_le32(tag + 12, f);
}

